#include "main.h"
#include <stdio.h>

/**
 * _memcpy - a function that copies memory area.
 * @src: a memory area
 * @n: number of bytes
 * @dest: the memory area.
 *
 * Return: memory area.
 */

char *_memcpy(char *dest, char *src, unsigned int n)
{
	int size = n;

	if (size > 0)
	{
		int i;

		for (i = 0; i < size; i++)
			dest[i] = src[i];
	}

	return (dest);
}
